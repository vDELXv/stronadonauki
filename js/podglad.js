function powiekszenie1(){
  let obrazy = document.getElementsByClassName("kolumna");
  if (obrazy[0].style.position=="fixed") {
    obrazy[0].style.position="static";
    obrazy[0].style.width="";
  }
  else{
    obrazy[0].style.position="fixed";
    obrazy[0].style.width="1000px";
  }
}
